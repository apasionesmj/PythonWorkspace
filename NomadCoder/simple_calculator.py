def plus(a, b):
    return int(a) + int(b)

def minus(a, b):
    return int(a) - int(b)

def multi(a, b):
    return int(a) * int(b)

def divide(a, b):
    return int(a) / int(b)

def negation(a, b):
    return int(a) // int(b)

def mod(a, b):
    return int(a) % int(b)

def sqareup(a, b):
    return int(a) ** int(b)

print(plus(2,"5"))
print(minus(2,"5"))
print(multi(2,"5"))
print(divide(2,"5"))
print(negation(2,"5"))
print(mod(2,"5"))
print(sqareup(2,"5"))